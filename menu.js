function change() {
	document.getElementById("menu").classList.toggle("change");
	// document.getElementById("options").classList.toggle("show");
	document.getElementById("options").style.width = 400;
}

function menuClose() {
	document.getElementById("menu").classList.toggle("change");
	// document.getElementById("options").classList.toggle("show");
	document.getElementById("options").style.width = 0;
}
	
